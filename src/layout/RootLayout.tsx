"use client";
import type { Metadata } from "next";
import { Montserrat } from "next/font/google";
import "@/app/globals.css";
import { PropsWithChildren } from "react";
import StoreProvider from "@/redux/storeProvider";
import { CssBaseline, StyledEngineProvider, ThemeProvider, createTheme, Box } from "@mui/material";

const montserrat = Montserrat({
  weight: ['100', '200', '300', '400', '500', '700'],
  subsets: ['latin'],
  display: 'swap',
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const theme = createTheme({
  palette: {
    mode: 'light',
  },
  typography: {
    fontFamily: montserrat.style.fontFamily,
  },
});

export default function RootLayout({ children, ...props }: PropsWithChildren) {
  return (
    <html lang="en">
      <meta
        name="viewport"
        content="width=device-width, user-scalable=no, initial-scale=1.0, minimum-scale=1.0, maximum-scale=10, minimal-ui"
      />
      <body
        className={`${montserrat.className}`}
      >
        <StyledEngineProvider injectFirst>
          <ThemeProvider theme={theme}>
            <CssBaseline />
            <StoreProvider>
              <Box>
                {children},
              </Box>
            </StoreProvider>
          </ThemeProvider>
        </StyledEngineProvider>
      </body>
    </html>
  );
}
